/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
	struct
	{
		uint32_t r0;
		uint32_t r1;
		uint32_t r2;
		uint32_t r3;
		uint32_t r12;
		uint32_t lr;
		uint32_t pc;
		uint32_t psr;
	} *stack_ptr;


	asm(
	    "TST lr, #4 \n" // test bit3 stack pointer
	    "ITE EQ \n"   // bit3 is true ?
	    "MRSEQ %[ptr], MSP  \n"  // if yes, save stack pointer
	    "MRSNE %[ptr], PSP  \n"  // if not, save process stack
	    : [ptr] "=r" (stack_ptr)
	    );

	char strBuffer[512];
	sprintf(strBuffer,
		"HardFault_Handler:\nStack_ptr: R0 = 0x%08lX, R1 = 0x%08lX, R2 = 0x%08lX, R3 = 0x%08lX, R12 = 0x%08lX, LR = 0x%08lX, PC = 0x%08lX, PSR = 0x%08lX\n",
		stack_ptr->r0,
		stack_ptr->r1,
		stack_ptr->r2,
		stack_ptr->r3,
		stack_ptr->r12,
		stack_ptr->lr,
		stack_ptr->pc,
		stack_ptr->psr);

	HAL_GPIO_WritePin(RS485_RTS_GPIO_Port, RS485_RTS_Pin, GPIO_PIN_SET);

	HAL_UART_Transmit(&huart1, (uint8_t*)strBuffer, strlen(strBuffer), HAL_MAX_DELAY);
	DWT_Delay_us(100);
	HAL_GPIO_WritePin(RS485_RTS_GPIO_Port, RS485_RTS_Pin, GPIO_PIN_RESET);

  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

